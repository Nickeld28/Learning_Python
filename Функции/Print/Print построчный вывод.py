# разные способы построчного вывода
# с разделителем
print('4', '\n8', '\n15', '\n16', '\n23', '\n42', sep='')
# без разделителя
print('4\n8\n15\n16\n23\n42')
# просто множество команд print
print(4)
print(8)
print(15)
print(16)
print(23)
print(42)
# можно еще проще через разделитель \n
print(4, 8, 15, 16, 23, 42, sep='\n')
# через тройные кавычки
print('''4
8
15
16
23
42''')
# через цикл for
for i in (4, 8, 15, 16, 23, 42):
    print(i)
