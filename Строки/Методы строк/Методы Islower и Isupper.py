domain1 = "www.yandex.ru"
domain2 = "www.youtube.com"
domain3 = "www.rutracker.org"
domain4 = "yandex.ua"

# Методы islower и isupper вызываются без параметров и возвращают "true" или "false".
# islower возвращает "true" если все символы в строке находятся только в нижнем регистре
# isupper возвращает "true" если все символы в строке находятся только в верхнем регистре

print(domain1.islower())
print(domain2.islower())
print(domain3.isupper())
print(domain4.isupper())
